# 연산자(operator)
# 산술 연산 - +, -, *, /, //(몫), %(나머지)
n1 = 10
n2 = 4

#산술연산
sum = n1 + n2
sub = n1 - n2
mul = n1 * n2
dev = n1 / n2


#출력 
print(n1, n2)   
print(sum)  
print(sub)  
print(mul)  
print(dev)  
print(n1 // n2)
print(n1 % n2)  


print(n1 ** n2) # 10000 거듭제곱
print(2 ** 3)

